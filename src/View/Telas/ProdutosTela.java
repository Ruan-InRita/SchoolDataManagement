    public void attTable(){
        ProdutoDAO pDAO = new ProdutoDAO();
        DescontoDAO ddao = new DescontoDAO();
        try{
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            int quant = jTable1.getRowCount();            
            for (int i=0;i<quant;i++){
                dtm.removeRow(0);
            }
            if (pDAO.buscarProduto() != null){
                for(Produtos lista : pDAO.buscarProduto()){
                    dtm.addRow(new Object[]{lista.getNome(),lista.getDescricao(),"R$ "+String.format("%.2f", lista.getPreco()),lista.getQuantidade(),ddao.buscarDesconto(lista.getId())+"%"});
                }
            } 
             
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    
    
