    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        
        FuncionarioDAO fdao = new FuncionarioDAO();
        
        
        try{
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            int quant = jTable1.getRowCount();            
            for (int i=0;i<quant;i++){
                dtm.removeRow(0);
            }
            if (fdao.Procurar(jTextField9.getText(),jTextField11.getText()) != null){
                for(Funcionario lista : fdao.Procurar(jTextField9.getText(),jTextField11.getText())){
                    dtm.addRow(new Object[]{lista.getNome(),lista.getTelefone(),lista.getCpf(),lista.getEndereco(), lista.getSalario()});
                }
            } 
             
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

